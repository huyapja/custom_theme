$(function () {
    frappe.call({
        method: "custom_theme.utils.get_active_theme_color",
        callback: function (r) {
            if (r.message) {
                const color = r.message;
                const colors = COLOR_DEFINE_RGB[color] ?? COLOR_DEFINE_RGB.sky;

                const bodyStyle = $("body").get(0).style;
                Object.keys(colors).forEach(level => {
                    bodyStyle.setProperty(`--custom-theme-${level}`, `rgb(${colors[level]})`);
                    bodyStyle.setProperty(`--custom-theme-rgb-${level}`, colors[level]);
                });
            }
        }
    });
});
